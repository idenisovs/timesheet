export const SECOND = 1000;
export const MINUTE = SECOND * 60;
export const HOUR = MINUTE * 60;
export const DAY = HOUR * 24;
export const WORK_DAYS = 5;
export const WORK_HOURS = 8;
export const WORK_DAY = WORK_HOURS * HOUR;
export const WORK_WEEK = WORK_DAYS * WORK_HOURS * HOUR;

export enum DAYS {
  SUNDAY,
  MONDAY,
  TUESDAY,
  WEDNESDAY,
  THURSDAY,
  FRIDAY,
  SATURDAY
}
